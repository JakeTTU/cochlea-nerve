function [ endolymph, perilymph ] = endolymph_perilymph()
%ENDOLYMPH_PERILYMPH

mM = Quantity(1e-3, 'M', 'mM');
g_per_l = Quantity(1, 'g_per_l', 'g_per_l');
mosm_per_l = Quantity(1, 'mosm_per_l', 'mosm_per_l');

endolymph = struct( ...
        'Na',           1    *mM, ...
        'K',          150    *mM, ...
        'Cl',         130    *mM, ...
        'Ca',           0.02 *mM, ...
        'glucose',      0.5  *mM, ...
        'proteins',     0.15 *g_per_l, ...
        'osmolarity', 315    *mosm_per_l, ...
        'pH',           7.4 ...
        );
    
perilymph = struct( ...
        'Na',         140    *mM, ...
        'K',            5    *mM, ... 4-5
        'Cl',         110    *mM, ...
        'Ca',           1.2  *mM, ...
        'glucose',      4    *mM, ...
        'proteins',     1    *g_per_l, ...
        'osmolarity', 290    *mosm_per_l, ...
        'pH',           7.4 ...
        );

end

